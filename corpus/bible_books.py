def build_name_index():
    """返回 {名称: (OSIS代号, 标准简称)},名称包含全名/别名/简称。"""
    index = {}
    for code, abbr, names in BOOKS:
        index[abbr] = (code, abbr)
        for n in names:
            index[n] = (code, abbr)
    return index


NAME_INDEX = build_name_index()

# 所有可识别名称,按长度降序(确保「约翰福音」优先于「约」)
ALL_NAMES = sorted(NAME_INDEX.keys(), key=len, reverse=True)
